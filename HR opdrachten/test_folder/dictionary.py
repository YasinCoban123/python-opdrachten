# Een dictionary met informatie over een student
student = {
    "naam": "Sophie",
    "leeftijd": 21,
    "studie": "Informatica",
    "cijfer_gemiddelde": 8.3
}

# Een dictionary in Python is een verzameling van key-value-paren, 
# waarbij elk item bestaat uit een sleutel (key) 
# en een waarde (value).

# In het voorbeeld hierboven:
# "naam" is een sleutel, 
# "Sophie" is de bijbehorende waarde.
# "leeftijd" is ook een sleutel,
# en 21 is de waarde die erbij hoort.
# Je gebruikt de sleutels om specifieke waardes op te halen. 
# Bijvoorbeeld:

print(student["naam"])  # Output: Sophie
